	<h3>Особенности</h3>
	<p><b>Галерея воспроизводит ВИДЕО</b> с: Youtube, Vimeo, HTML5, Wistia и SoundCloud (не видео, но все же)</p>
	<p><b>Отзывчивый</b> - подходит для каждого экрана с автоматическим сохранением соотношения</p>
	<p><b>Touch Enabled</b> - каждая часть галереи может управляться сенсорными устройствами с сенсорным экраном</p>
	<p><b>Отзывчивый</b> - галерея может соответствовать размеру экрана и реагировать на изменение размера экрана.</p>
	<p><b>Skinnable</b> - Позволяет легко менять скин в другом файле CSS, не касаясь основной галереи CSS.
	<p><b>Themable</b> - галерея имеет различные темы, каждая тема имеет свои собственные параметры и функции, но она использует основные объекты галереи</p>
	<p><b>Эффект масштабирования</b> - галерея имеет уникальный эффект масштабирования, который можно применить с помощью кнопок, колесика мыши или жеста повышения на устройствах с сенсорным экраном</p>
	<p><b>Кнопки галереи</b> - в галерее есть кнопки, такие как полноэкранный режим или воспроизведение / пауза, оптимизированные для доступа сенсорных устройств</p>
